package Table;

public class Teacher {

    public int idTeacher;
    public String name;
    public String surname;
    public String birth;
    public String number;

    public Teacher() {

    }

    public Teacher(int idTeacher, String name, String surname, String birth, String number) {
        this.idTeacher = idTeacher;
        this.name = name;
        this.surname = surname;
        this.birth = birth;
        this.number = number;
    }
}
